    public void cargarProductos() {
        while (modelo.getRowCount()>0) {
            modelo.removeRow(0);
        }
        
        for (Producto producto : Principal.productos) {
            Object prod[] = new Object[6];
            prod[0] = producto.getID();
            prod[1] = producto.getProveedor();
            prod[2] = producto.getPrecioC();
            prod[3] = producto.getPrecioV();
            prod[4] = producto.getStock();
            prod[5] = producto.getDesc();
            modelo.addRow(prod);
        }
        
        tblProductos.setModel(modelo);
    }
